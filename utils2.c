#include "push_swap.h"

int	check_space(char *s)
{
	int	i;

	i = (int)ft_strlen(s);
	if (i == 0)
		return (0);
	i = -1;
	while (s[++i])
	{
		if (s[i] != ' ')
			return (1);
	}
	return (0);
}
