#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		num;
	char	c;

	num = n;
	if (num < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
			return ;
		}
		write(fd, "-", 1);
		ft_putnbr_fd(-num, fd);
	}
	else if (num < 10)
	{
		c = num + '0';
		write(fd, &c, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
