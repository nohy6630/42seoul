#include "libft.h"

void	*ft_memchr(const void *b, int c, size_t len)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)b;
	while (i < len)
	{
		if (str[i] == c)
			break ;
		i++;
	}
	if (i >= len)
		return (NULL);
	else
		return (str + i);
}
